public class JZ_68_II {
    /**
     * 核心思路：前提条件是二叉搜索树，p 和 q 要么位于同一侧，要么位于这个父节点的两侧
     *
     * @param root
     * @param p
     * @param q
     * @return
     */
    public TreeNode lowestCommonAncestor(TreeNode root, TreeNode p, TreeNode q) {
        // 递归终止条件判断，如果返回 null，就是知道叶子节点也没有找到 q p。
        if (root == null || root == p || root == q) {
            return root;
        }
        TreeNode left = lowestCommonAncestor(root.left, p, q);
        TreeNode right = lowestCommonAncestor(root.right, p, q);

        // p q 一个在左，一个在右
        if (left != null && right != null) {
            return root;
        }
        // p q 都在左
        if (left != null) {
            return left;
        }
        // p q 都在右
        if (right != null) {
            return right;
        }
        return null;
    }

    static class TreeNode {
        int val;
        TreeNode left;
        TreeNode right;

        TreeNode(int x) {
            val = x;
        }
    }
}
